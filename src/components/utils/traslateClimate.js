export function translateClimate(name){
    switch(name){
        case "Snow":
            return "Neve"
        case "Sleet":
            return "Granizo"
        case "Hail":
            return "Saudação"
        case "Thunderstorm":
            return "Trovoada"
        case "Heavy Rain":
            return "Chuva pesada"
        case "Light Rain":
            return "Chuva leve"
        case "Showers":
            return "Chuveiro"
        case "Heavy Cloud":
            return "Nuvem Pesada"
            case "Light Cloud":
            return "Parcialmente nublado"
        case "Clear":
            return "Claro"
    }
}